export default function AppBottomNav() {
  const { pathname } = useLocation();
  const queryClient = useQueryClient();
  const { isLoggedIn, userType, selectedRole } = useAuth();

  const items = getBottomNavItems({ isLoggedIn, userType, selectedRole });
  if (items.length === 0) return null;

  const prefetchStudentDashboard = () => {
    if (userType !== 'student' || !isLoggedIn) return;
    queryClient.prefetchQuery({
      queryKey: queryKeys.studentProfile,
      queryFn: () => apiClient.get('/student/profile'),
      staleTime: 2 * 60 * 1000,
    });
    queryClient.prefetchQuery({
      queryKey: queryKeys.studentDashboardStats,
      queryFn: () => apiClient.get('/student/dashboard/stats'),
      staleTime: 60 * 1000,
    });
  };

  return (
    <nav
      className="fixed inset-x-0 bottom-0 z-50 safe-area-bottom bg-slate-900/95 backdrop-blur-xl border-t border-slate-700/50"
      aria-label="Main navigation"
    >
      <div className="flex items-stretch justify-around h-16 max-w-lg mx-auto">
        {items.map((item) => {
          const active = item.match(pathname);
          return (
            <Link
              key={item.path}
              to={item.path}
              onMouseEnter={item.path === '/student/dashboard' ? prefetchStudentDashboard : undefined}
              onFocus={item.path === '/student/dashboard' ? prefetchStudentDashboard : undefined}
              className={`flex flex-1 flex-col items-center justify-center gap-0.5 text-xs font-medium transition-colors ${
                active ? 'text-purple-400' : 'text-slate-400 hover:text-slate-200'
              }`}
            >
              <NavIcon name={item.icon} />
              <span>{item.label}</span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}
